import numpy as np

def degreeSparsity(mtrx):
    cont = 0
    i = 0
    while i < len(mtrx):
        j = 0
        while j < len(mtrx[i]):
            if mtrx[i][j] == 0:
                cont = cont + 1
            j = j + 1
        i = i + 1
    return ((cont / (len(mtrx) * len(mtrx[0]))) * 100)

